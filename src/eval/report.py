"""Write compact validation artifacts for the sponsor deck."""

from __future__ import annotations

import json
from pathlib import Path

import matplotlib.pyplot as plt
import pandas as pd


def write_report(*, event_rows: list[dict], gates: list[dict], metrics: dict, artifact_dir: str | Path = "models/eval") -> Path:
    destination = Path(artifact_dir); destination.mkdir(parents=True, exist_ok=True)
    events = pd.DataFrame(event_rows); gate_frame = pd.DataFrame(gates)
    events.to_csv(destination / "six_event_backtest.csv", index=False)
    gate_frame.to_csv(destination / "gate_results.csv", index=False)
    if not events.empty:
        fig, ax = plt.subplots(figsize=(9, 4)); ax.bar(events["date"], events["forecast_surprise_zscore"], color="#00B4D8")
        ax.axhline(0, color="black", lw=.8); ax.set(title="Forecast-surprise z-score at crisis dates", ylabel="z-score")
        plt.xticks(rotation=35, ha="right"); fig.tight_layout(); fig.savefig(destination / "forecast_surprise_crises.png", dpi=160); plt.close(fig)
    # Reuse the exact held-out classifier artifacts generated by Module 2.
    source = Path("models")
    for name in ("confusion_matrix_test.csv", "calibration_curve_test.csv"):
        if (source / name).exists(): (destination / name).write_bytes((source / name).read_bytes())
    report = {"headline_metrics": metrics, "backtest": event_rows, "gates": gates}
    path = destination / "validation_report.json"; path.write_text(json.dumps(report, indent=2, allow_nan=True), encoding="utf-8")
    return path
